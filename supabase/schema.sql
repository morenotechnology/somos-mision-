create or replace function public.initials_from_name(full_name text)
returns text
language sql
immutable
as $$
  select upper(
    left(coalesce(split_part(trim(full_name), ' ', 1), 'U'), 1) ||
    left(coalesce(nullif(split_part(trim(full_name), ' ', 2), ''), split_part(trim(full_name), ' ', 1)), 1)
  );
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  meta jsonb := coalesce(new.raw_user_meta_data, '{}'::jsonb);
  full_name text := coalesce(meta ->> 'nombre_completo', meta ->> 'name', split_part(new.email, '@', 1));
  assigned_role public.app_role := 'multiplicador';
begin
  if meta ->> 'rol' = 'pastor' and meta ->> 'pastor_access_key' = 'IPUC2026MISION' then
    assigned_role := 'pastor';
  end if;

  insert into public.profiles (
    id,
    nombre,
    nombre_completo,
    email,
    rol,
    region_id,
    district_id,
    congregacion,
    cargo,
    celular,
    whatsapp,
    avatar,
    avatar_color,
    cuenta_activa
  )
  values (
    new.id,
    coalesce(meta ->> 'nombre', split_part(full_name, ' ', 1)),
    full_name,
    new.email,
    assigned_role,
    nullif(meta ->> 'region_id', ''),
    nullif(meta ->> 'district_id', ''),
    nullif(meta ->> 'congregacion', ''),
    nullif(meta ->> 'cargo', ''),
    nullif(meta ->> 'celular', ''),
    nullif(meta ->> 'whatsapp', ''),
    public.initials_from_name(full_name),
    coalesce(nullif(meta ->> 'avatar_color', ''), '#1A237E'),
    true
  )
  on conflict (id) do update
    set email = excluded.email,
        nombre = excluded.nombre,
        nombre_completo = excluded.nombre_completo,
        rol = excluded.rol,
        region_id = excluded.region_id,
        district_id = excluded.district_id,
        congregacion = excluded.congregacion,
        cargo = excluded.cargo,
        celular = excluded.celular,
        whatsapp = excluded.whatsapp;

  insert into public.profile_badges (profile_id, badge_id)
  values (new.id, 'b1')
  on conflict do nothing;

  return new;
end;
$$;

