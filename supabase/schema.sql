create or replace function public.apply_xp(p_profile_id uuid, p_points integer, p_action text, p_reference_type text default null, p_reference_id text default null)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.profiles
  set xp = xp + greatest(p_points, 0)
  where id = p_profile_id;

  insert into public.xp_activities (profile_id, accion, puntos_ganados, reference_type, reference_id)
  values (p_profile_id, p_action, greatest(p_points, 0), p_reference_type, p_reference_id);
end;
$$;

create or replace function public.refresh_profile_badges(p_profile_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_xp integer;
  v_shares integer;
  v_missions integer;
begin
  select xp into v_xp from public.profiles where id = p_profile_id;
  select count(*) into v_shares from public.shares where user_id = p_profile_id;
  select count(*) into v_missions from public.mission_completions where profile_id = p_profile_id;

  if v_shares >= 10 then
    insert into public.profile_badges (profile_id, badge_id) values (p_profile_id, 'b2') on conflict do nothing;
  end if;

  if v_xp >= 1000 then
    insert into public.profile_badges (profile_id, badge_id) values (p_profile_id, 'b4') on conflict do nothing;
  end if;

  if v_missions >= 5 then
    insert into public.profile_badges (profile_id, badge_id) values (p_profile_id, 'b5') on conflict do nothing;
  end if;
end;
$$;

create or replace function public.share_publication(p_publication_id bigint, p_red_social text default 'whatsapp')
returns table(shared_id uuid, xp_ganado integer, publication_shares integer, profile_xp integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_exists uuid;
  v_xp integer;
begin
  if v_user is null then
    raise exception 'No authenticated user';
  end if;

  select id into v_exists
  from public.shares
  where publication_id = p_publication_id and user_id = v_user;

  if v_exists is null then
    insert into public.shares (publication_id, user_id, social_network)
    values (p_publication_id, v_user, coalesce(p_red_social, 'whatsapp'))
    returning id into v_exists;

    update public.publications
    set shares_count = shares_count + 1
    where id = p_publication_id
    returning xp_reward into v_xp;

    perform public.apply_xp(v_user, coalesce(v_xp, 0), 'contenido_compartido', 'publication', p_publication_id::text);
    perform public.refresh_profile_badges(v_user);
  else
    select xp_reward into v_xp from public.publications where id = p_publication_id;
    v_xp := 0;
  end if;

  return query
  select
    v_exists,
    coalesce(v_xp, 0),
    (select shares_count from public.publications where id = p_publication_id),
    (select xp from public.profiles where id = v_user);
end;
$$;

create or replace function public.complete_mission_action(p_mission_id uuid)
returns table(completion_id uuid, xp_ganado integer, total_completions integer, profile_xp integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_exists uuid;
  v_xp integer;
begin
  if v_user is null then
    raise exception 'No authenticated user';
  end if;

  select id into v_exists
  from public.mission_completions
  where mission_id = p_mission_id and profile_id = v_user;

  if v_exists is null then
    insert into public.mission_completions (mission_id, profile_id)
    values (p_mission_id, v_user)
    returning id into v_exists;

    select xp_reward into v_xp from public.missions where id = p_mission_id;
    perform public.apply_xp(v_user, coalesce(v_xp, 0), 'mision_completada', 'mission', p_mission_id::text);
    perform public.refresh_profile_badges(v_user);
  else
    v_xp := 0;
  end if;

  return query
  select
    v_exists,
    coalesce(v_xp, 0),
    (select count(*)::integer from public.mission_completions where profile_id = v_user),
    (select xp from public.profiles where id = v_user);
end;
$$;

create or replace function public.get_my_activity()
returns table(shared_publication_ids bigint[], completed_mission_ids uuid[])
language sql
security definer
set search_path = public
as $$
  select
    coalesce(array_agg(distinct s.publication_id) filter (where s.publication_id is not null), '{}'::bigint[]) as shared_publication_ids,
    coalesce(array_agg(distinct mc.mission_id) filter (where mc.mission_id is not null), '{}'::uuid[]) as completed_mission_ids
  from public.profiles p
  left join public.shares s on s.user_id = p.id
  left join public.mission_completions mc on mc.profile_id = p.id
  where p.id = auth.uid()
  group by p.id;
$$;

