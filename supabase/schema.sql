create or replace function public.get_weekly_activity()
returns table(day text, shares bigint, xp bigint)
language sql
security definer
set search_path = public
as $$
  with days as (
    select generate_series(date_trunc('day', now()) - interval '6 day', date_trunc('day', now()), interval '1 day')::date as d
  )
  select
    to_char(days.d, 'Dy') as day,
    coalesce(count(distinct s.id), 0) as shares,
    coalesce(sum(x.puntos_ganados), 0) as xp
  from days
  left join public.shares s on date_trunc('day', s.created_at)::date = days.d
  left join public.xp_activities x on date_trunc('day', x.fecha)::date = days.d
  group by days.d
  order by days.d;
$$;

create or replace function public.get_region_activity()
returns table(name text, embajadores bigint, compartidos bigint)
language sql
security definer
set search_path = public
as $$
  select
    r.name,
    count(distinct p.id) as embajadores,
    coalesce(count(distinct s.id), 0) as compartidos
  from public.regions r
  left join public.profiles p on p.region_id = r.id
  left join public.shares s on s.user_id = p.id
  group by r.id, r.name
  order by r.name;
$$;

